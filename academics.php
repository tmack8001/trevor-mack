							<h4>College Overview</h4>
							<p>The B. Thomas Golisano College of Computing and Information Sciences, also known as "GCCIS". GCCIS is the second largest and one of the newest colleges at RIT. In the late 1990s, the Dean of the College of Applied Science and Technology, "CAST", made a proposition to create another college that would focas on the growing fields of computer science and technology. Soon after B. Thomas Golisano, the Chairman and CEO of Paychex, Inc., decided to help fund this adventure with a generous offer of $14 million dollars to help with the college's creation. Thus having the school named after himself and being offically added to the prestigious colleges at RIT on July 1st, 2001. The computer science department which has grown since its inception in the early 1970s has grown to become one of the largest in the United States.</p>
