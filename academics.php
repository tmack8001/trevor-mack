							<h4>University Overview</h4>
							<p>Rochester Institute of Technology, known as RIT, is a private university, with emphasis put on undergraduate education. RIT is located in Henrietta, a suburban town of Rochester, NY. It was first founded in downtown Rochester in 1829 and moved to the new campus in Henrietta in 1968. </p>
							<p>RIT is well-known for its high-class information technology, imaging, engineering, art, and photography undergraduate programs. It also has one of the oldest and largest cooperative education programs in the United States of America. In the program students hold a full-time job for a period, outside of taking classes, as part of their degree requirements. RIT is also one of only a couple of schools left that have their school year divided into a quarter system instead of the more popular semesters. This is to make it easier on students to adapt and complete their cooperative requirements.</p>
							<p>RIT's co-op program, which began in 1912, is the fourth oldest in the world. It is also the fifth largest in the nation, with approximately 3,500 students completing a co-op each year at over 2,000 businesses. This program requires, depending on major, students to work in the workplace for one or two quarters alternating with quarters of class. The program helps students to understand and define their profession career path for specifically than they could ever do in class alone. The amount of co-op varies, but usually between 3 and 5 three-month academic quarters. Many employers prefer students to co-op for two consecutive quarters, referred to as a "double-block co-op". During a co-op block, the student is not required to pay tuition to the school and is still considered a full time student.</p>
							<p>Because many majors require at least 40 weeks, or 4 "blocks" of co-op experience, the majority of undergraduate degree programs at RIT require five years to complete.</p>
