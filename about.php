							 <p>My name is Trevor Mack as you should have already known, since you found my site. Most people call me Trevor, but I also have gone by other nicknames: Trev, BigGuy, TMaC. I will mostly answer to anything. I am a recent graduate of the Rochester Institute of Technology (RIT) in Rochester New York with a dual undergraduate degree in Computer Science and Economics. I finished my dual degree in just over four years, that is a five year program finished in four. This was accomplished through earned credit from AP exams in high school. I maintained a cumulative 3.20 overall GPA while obtaining 183 credit hours. Upon graduation from RIT in August/September 2010 I took on a Software Systems Engineer position at the MITRE Corporation in Boston, MA which is where I am currently residing.</p>
