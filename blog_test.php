							<h2 class="blog-title"><a href="http://trevor-mack.com/blog/2010/03/18">My Personal Faith Story</a></h2> 
							<div class="blog-header"> 
								<span class="blog-stats"> 
									Author: <a class="author" href="http://trevor-mack.com/about">Trevor Mack</a> 
									posted <span class="blog-date">April 7th, 2011</span> 
