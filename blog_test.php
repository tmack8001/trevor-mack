									</span> 
<div> 
<blockquote style="width: 70%;">"For through the law I died to the law so that I might live for God. I have been crucified with Christ and I no longer live, but Christ lives in me. The life I now live in the body, I live by faith in the Son of God, who loved me and gave himself for me. I do not set aside the grace of God, for if righteousness could be gained through the law, Christ died for nothing!" - Galatians 2:19-21</blockquote>
<p>My faith story begins like everyone else's ... at the beginning. From birth my family was a "Christian household" at least as far as going to Church a couple times a year goes. Growing up in upstate New York there were churches everywhere so it was convenient. Even though I don't remember living there, since we moved to Tennessee before I was two years old, it stayed with me as I visited distant relatives over the summer (grandparents know how to spoils grandchildren by waking them up to go to Church early Sunday mornings).</p>
<p>Living the majority of my life in Tennessee (in a region known by non-other than "The Bible Belt") you would have thought that it would be even easier for my family to attend church on a regular basis or at least on the same schedule as before... but this couldn't be further from the truth. Throughout my childhood I only ever remember going to church during the Christmas season (or what my parents abbreviated as X-Mas). It almost seems that as if both my parents, without the rest of their family around, grew out of the church scene altogether.</p>
<p>While in middle school I made friends with another student who was in the majority of the same classes as me, Jeremy. We had grown pretty close together as we hung out during recess, lunch and even sometimes do homework together during homeroom. As the school year was coming to a close he mentioned that he was going to camp over the summer and that I was more than welcome to come along if I wanted to. The camp that he was referring to was Buffalo Mountain United Methodist Camp (an outreach of the Holston Conference Camp and Retreat Ministries). Needless to say, that summer both Jeremy and I went to Buffalo Mountain Camp together. It might have been this experience which fueled my passion for the outdoors and camping (it has even stayed with me until now ... nothing beats worshiping and giving praise to God amongst His glorious creation!). As their website (though it might not have existed back when I attended) says "It's a great time for campers to: play in the creek, swim in the pool ... But most importantly, we want each and every camper to learn more about who Jesus is and [how] much He loves them. At Buffalo Mountain Camp we mesh daily Bible study and worship with opportunities to experience the outdoors in new and exciting ways."</p>
<p>Though I didn't realize it at the time, it was these experiences at Buffalo Mountain Camp that eventually led me down the path to Salvation in our Lord Jesus Christ. After a number of years attending the camp and going to Church maybe three or four times a year, I began to question other students at school if their families went to Church together (not that I really knew what it was). The majority of my friends at the time responded: "yea, we go every Sunday" or "we might go every couple of weeks" or even "that building that my parents drag me along with them? yes ... unfortunately". It seemed as if this was a thing that everyone around me was doing ... so I started to wonder "Why don't we go to church?" You could say that this was the "turning point" when I became a "seeker" though I wasn't sure who or what I was seeking.</p>
<p>Randomly one night, on the way home from a sporting event, I queried my Dad why we never go to Church on Sunday. The answer he gave was very much unexpected "I don't know; we just never found a place where we fit in." Though I don't know the validity of this statement as I was much too young to remember, as far as I could remember we only attended Church in Tennessee during the Christmas season. The next thing that happened was even more surprising since soon after my Dad and I would start going around to different Churches close by over the coming weeks. Quickly we stumbled upon a relatively large Church, Heritage Baptist Church, which was actually right across the street from the High School that I would in just a few years be attending.</p> 
<span style="padding: 10px 10px 5px 0px; float: left;"> 
 <a href="http://www.hbcjc.org"><img width="140px" title="Heritage Baptist Church" alt="Heritage Baptist Church" src="\images\blog\heritage_baptist_church.jpg"/></a>
</span>
<p>It was this Church, where I would eventually be baptized and start my "service career" on the tech team. It was this Church where I would be spiritually disciplined. It was this Church where I would be molded into the person I am today. It was this Church where I found true happiness and once again knew that everything was going to work out for good. It was this Church where I ... found myself welcomed, found myself accepted, found myself wanted, found myself to be loved. To put this into perspective, during this time my parents were almost always fighting even to the point where I called the police because I heard excessive yelling downstairs (no Mom and Dad, it wasn't my older brother who "accidentally called 911"). There were a number of nights when I couldn't get to sleep and in my frustration and anger cried out to this God I had heard about to do whatever it takes to make it quite again (granted at that time in my anger I only gave Him two options ... and thankfully He didn't agree that those were the best for anyone).</p>
<span style="padding: 10px 0px 5px 10px; float: right;"> 
 <a href="http://www.thirdday.com"><img width="150px" title="Third Day - Mac Powell" alt="Third Day - Mac Powell" src="\images\blog\mac_powell.jpg"/></a>
</span>
<p>As I became more assimilated to the "Christian way of life", going to Church on Sundays, I became very fond of Christian music and used it primarily to drown out the background noises of my family life. The band that I listened to more than any other was none other than Third Day. Eventually this love for Third Day's music drew me to go to one of their concerts near my hometown of Johnson City, TN. This concert sticks out very well in my mind as I wouldn't be who I am today if it wasn't for this particular concert. Suddenly in the middle of the concert all music stopped and amongst the silence in the arena the main singer, Mac Powell, grabbed his microphone and started to tell a story about his life (his personal testimony).  I rationally figured that if the band was going to stop preforming in the middle of a concert then whatever Mac was saying was going to be important so thus I paid close attention contemplating every word. At this point he shares about his adoption through Bethany Christian Services and related that to how God wants to adopt us to His own children. This really hit me hard since ultimately I made the conscious decision that I no longer could live the way that I had been and that the only way to find true peace, love and job was to surrender my life over to Jesus Christ. At the end of his testimony he asked if anyone was ready to become a child of God, and I was so ready to be a part of the loving family of God that I dropped to my knees and gave myself over to God's provision and protection of my life. He didn't just leave it at that he walked through "The Sinner's Prayer" and told anyone that was ready to make sure that they let someone close to them know or to go to the back of the arena and someone would talk with you (promising that you wouldn't miss any of the concert since at that point there was a World Vision video and announcement from stage). I however, wasn't quite ready to go talk with someone while tears were rolling down my cheeks I just sat there in relief and pure gladness to know that I had a Father in Heaven that loved and cared about me. This was the turning point in my life and I knew right then that I would never be the same again.</p>
<p>After that concert and the following Wednesday Youth group meeting where I made my Youth Pastor aware of my life changing decision I obtained an overwhelming urge to get involved everywhere I possibly could. At this point I got heavily involved in the tech team at Heritage as well as the Youth ministry. After a few years I was involved almost everywhere I could think of and imagine: VBS, Sunday Morning Worship, KidStuff (Sunday Night Children's Ministry), Wednesday Youth Group (High School Ministry), Summer Mission Trips, Club 121 Leader (Christian Club at HS on Fridays), and meeting with my Youth Pastor multiple times a week. During this time I came to realize what gift God gave me as a spiritual gift ... and multiple people have said that they are very obvious because I practice them almost every day: Service and Giving. Ever since then I have been giving of myself and my time over to the LORD in service of furthering His kingdom. No matter where I went whatever church I was a part of at the time I would quickly transition to assist them with the technology aspect of their worship settings and in multiple instances working to enhance their current technology footprint.</p>
<span style="padding: 10px 0px 5px 10px; float: right;"> 
 <a href="http://pittsfordbaptistchurch.lifewaylink.com/"><img width="350px" title="Pittsford Baptist Church" alt="Pittsford Baptist Church" src="\images\blog\pittsford_baptist_church.jpg"/></a>
</span>
<p>When it came time to leave my family, both my actual family as well as my new found spiritual family, to go to college I felt that I had a firm foundation to continue my faith boldly no matter where I was. I found myself being pulled towards RIT in Rochester, New York (thus the only school I applied to trusting fully in God). Even prior to my arrival I had already found a student group that I was going to be a part of as well as a church that I was going to attend while I was there (yay growing up the in days of the Internet). Proactive I know, but I knew that if I wasn't going to be proactive about living for God right off the bat the same thing that happened to my parents would happen to me. Thus I reached out to the Baptist Campus Minister at the time, David Skeldon, and he told me about Pittsford Baptist Church that there were car pools for leaving from the dorms ... man couldn't get more convenient than that (except for the fact that I had a car ... thus I volunteered to help drive; see the service trait coming out again). I have to honestly say that over the next four years I grew the most in my faith than I had ever in the years prior. Within one year of being a part of BCM and with the help of Dave's subliminal messaging tactics (those around in Dave's days will know what I am talking about) I became a co-bible study leader with Shijo George as well as went on the Spring Break missions trip to Puerto Rico then I spent the summer back in Tennessee and was asked if I would chaperone the Youth Missions Trip to Donna, TX  in support of a couple of HBC's Mexican Missionaries, Jaun and Santiago Curling, at the Macedonian Christian Academy.</p>
<span style="padding: 10px 10px 5px 0px; float: left;"> 
 <a href="http://www.grace.com"><img width="250px" title="Grace Chapel" alt="Grace Chapel" src="\images\blog\grace_chapel.jpg"/></a>
</span>
<p>Upon graduation from college I received a full-time job with the same company I had a co-op/internship with the prior year in Bedford, MA when I first attended Grace Chapel and actively became a part of the FIRE ministry. When I had to go back to college after one of the most awesome summers of my life my heart was still with the ministry and the new friends I had left in Boston so I was ecstatic when I heard the news I would be moving back permanently. So shortly after graduation I moved back out to the Boston area and quickly got back involved with the FIRE ministry. Not soon after moving back I heard that the gc@nite tech team needed some assistance so I started asking for additional information and eventually got involved with gc@nite which led to me ultimately being involved in the morning services. Ever since I have played a large role on the tech team here at Grace Chapel and have thoroughly enjoy every minute of it. From the 5,000+ people for the five Christmas Eve services to the 3,000 average people on any given Sunday. This is where God has given me a passion to serve and I do so routinely</p>
